package com.prelert.jetty;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.eclipse.jetty.server.Request;
import org.eclipse.jetty.server.handler.ErrorHandler;

/**
 * A Servlet error handler that performs no extra processing.
 * The main purpose of this class is to override the default
 * Jetty implementation which returns Jetty branded HTML. 
 */
public class ApiErrorHandler extends ErrorHandler 
{
	@Override
	public void handle(String target, Request baseRequest, HttpServletRequest request, 
			HttpServletResponse response)
	{
		// do nothing to the response object
	}


}
