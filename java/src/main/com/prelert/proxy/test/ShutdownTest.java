package com.prelert.proxy.test;

import static org.junit.Assert.assertTrue;

import java.net.MalformedURLException;
import java.rmi.NotBoundException;
import java.rmi.RMISecurityManager;
import java.rmi.RemoteException;
import java.rmi.registry.Registry;

import org.junit.BeforeClass;
import org.junit.Test;

import com.prelert.proxy.dao.RemoteControlDAO;

public class ShutdownTest 
{
	private static RemoteControlDAO s_RemoteDAO;
	
	@BeforeClass
	public static void oneTimeSetup() throws RemoteException, MalformedURLException, NotBoundException, Exception
	{
		// Create and install a security manager
		if (System.getSecurityManager() == null)
		{
			System.setSecurityManager(new RMISecurityManager());
		}	

		Registry registry = LocateRegistryForTests.getRegistry();
		s_RemoteDAO = (RemoteControlDAO)registry.lookup("com.prelert.ControlServer");
	}
	
	// time out after 5mins
	@Test(timeout=300000) 
	public void testShutdown() throws RemoteException
	{
		boolean result = s_RemoteDAO.shutdown();
		
		assertTrue(result);		
	}
}
